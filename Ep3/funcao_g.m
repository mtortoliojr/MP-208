%%--------------------------------------------------------------------------------
%% Função para o cálculo de g(x)
%%--------------------------------------------------------------------------------
function g = funcao_g(x)

% Componente alfa de x
alfa = x(7:9);

% Cálculo da matrix DPG e Alfa
DPG = Matriz_DPG(alfa);
A = Matriz_A(alfa);

% --------------------------------------------------------------
% g(x)
% --------------------------------------------------------------
g = zeros(9,6);
g(4:6,1:3) = -DPG';
g(7:9,4:6) = -A;

end
