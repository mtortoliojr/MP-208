%%--------------------------------------------------------------------------------
%% Função para o cálculo de h(x,pG)
%%--------------------------------------------------------------------------------
function h = funcao_h(x,pG)

% Número de landmarks
q = size(pG,2);

% --------------------------------------------------------------
% h = [h1; ...; hn]
% --------------------------------------------------------------
h = [];
for i = 1:q
	hi = funcao_hi(x,pG(:,i));
	h = [h; hi];
end

end
